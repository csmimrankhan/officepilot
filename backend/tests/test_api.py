def _make_pdf_bytes() -> bytes:
    """Build a real text PDF using PyMuPDF (already a dependency).

    Single page with the fields the parser expects.
    """
    import fitz
    doc = fitz.open()
    page = doc.new_page(width=612, height=792)
    text = (
        "ACME Office Supplies\n"
        "123 Industrial Way\n"
        "Springfield, IL 62704\n"
        "\n"
        "INVOICE\n"
        "\n"
        "Invoice Number: INV-2026-0042\n"
        "Invoice Date: 2026-05-12\n"
        "Due Date: 2026-06-11\n"
        "\n"
        "Description                  Qty   Unit Price   Line Total\n"
        "Printer Paper A4            10    4.50         45.00\n"
        "Toner Cartridge HP 26X      4     89.00        356.00\n"
        "Stapler Heavy Duty          2     14.75        29.50\n"
        "\n"
        "Subtotal: 430.50\n"
        "Tax (7%): 30.14\n"
        "Total: $460.64\n"
    )
    page.insert_text((50, 60), text, fontsize=11)
    data = doc.tobytes()
    doc.close()
    return data


