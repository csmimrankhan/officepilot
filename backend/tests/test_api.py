def test_upload_text_pdf_extracts_fields(client: TestClient):
    pdf = _make_pdf_bytes()
    r = client.post(
        "/api/invoices/upload",
        files={"file": ("sample.pdf", io.BytesIO(pdf), "application/pdf")},
    )
    assert r.status_code == 201, r.text
    data = r.json()
    assert data["invoice_number"] == "INV-2026-0042"
    assert data["total_amount"] == 460.64
    assert data["currency"] == "USD"
    assert data["confidence_score"] > 0
    # Phase 3 renamed PENDING → READY_FOR_APPROVAL. Either of these is
    # acceptable depending on whether the parser cleanly extracted the
    # synthetic PDF.
    assert data["status"] in ("ready_for_approval", "needs_review")


def test_upload_image_creates_invoice_with_extraction_warnings(client: TestClient):
    jpg = _make_jpeg_bytes()
    r = client.post(
        "/api/invoices/upload",
        files={"file": ("scan.jpg", io.BytesIO(jpg), "image/jpeg")},
    )
    assert r.status_code == 201, r.text
    data = r.json()
    assert data["status"] in ("ready_for_approval", "needs_review")
    # OCR is disabled in tests, so we expect a needs_review status.
    assert data["status"] == "needs_review"
    assert any("OCR" in w or "Image" in w for w in data["warnings_json"])


def test_edit_invoice_and_audit_log(client: TestClient):
    pdf = _make_pdf_bytes()
    r = client.post(
        "/api/invoices/upload",
        files={"file": ("a.pdf", io.BytesIO(pdf), "application/pdf")},
    )
    inv_id = r.json()["id"]

    r2 = client.patch(f"/api/invoices/{inv_id}", json={"vendor_name": "Edited Co."})
    assert r2.status_code == 200, r2.text
    assert r2.json()["vendor_name"] == "Edited Co."

    logs = client.get("/api/audit-logs", params={"entity_id": inv_id}).json()
    actions = {l["action"] for l in logs}
    assert "upload" in actions
    assert "extraction" in actions
    assert "edit" in actions


def test_approve_then_export_excel(client: TestClient):
    pdf = _make_pdf_bytes()
    r = client.post(
        "/api/invoices/upload",
        files={"file": ("a.pdf", io.BytesIO(pdf), "application/pdf")},
    )
    inv_id = r.json()["id"]

    # Edit fields to make sure required ones are present (PDFs are synthetic, so we
    # make the data deterministic before approving).
    client.patch(
        f"/api/invoices/{inv_id}",
        json={
            "vendor_name": "ACME Office Supplies",
            "invoice_number": "INV-2026-0042",
            "invoice_date": "2026-05-12",
            "due_date": "2026-06-11",
            "currency": "USD",
            "subtotal": 430.50,
            "tax": 30.14,
            "total_amount": 460.64,
        },
    )
    r2 = client.post(f"/api/invoices/{inv_id}/approve")
    assert r2.status_code == 200, r2.text
    assert r2.json()["status"] == "approved"

    r3 = client.get("/api/invoices/export/excel")
    assert r3.status_code == 200
    assert r3.headers["content-type"].startswith(
        "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
    )
    # The body should be a real .xlsx (PK\x03\x04 ZIP header).
    assert r3.content[:4] == b"PK\x03\x04"


def test_reject_blocks_export(client: TestClient):
    pdf = _make_pdf_bytes()
    r = client.post(
        "/api/invoices/upload",
        files={"file": ("a.pdf", io.BytesIO(pdf), "application/pdf")},
    )
    inv_id = r.json()["id"]
    client.patch(
        f"/api/invoices/{inv_id}",
        json={"vendor_name": "ACME", "invoice_number": "X1", "invoice_date": "2026-01-01",
              "currency": "USD", "total_amount": 1.0},
    )
    r2 = client.post(f"/api/invoices/{inv_id}/reject", params={"reason": "test"})
    assert r2.status_code == 200
    assert r2.json()["status"] == "rejected"

    # Exporting an Excel file should succeed but contain no rows for this invoice.
    r3 = client.get("/api/invoices/export/excel")
    assert r3.status_code == 200


def test_duplicate_upload_marked(client: TestClient):
    pdf = _make_pdf_bytes()
    r1 = client.post(
        "/api/invoices/upload",
        files={"file": ("a.pdf", io.BytesIO(pdf), "application/pdf")},
    )
    assert r1.status_code == 201
    r2 = client.post(
        "/api/invoices/upload",
        files={"file": ("a-copy.pdf", io.BytesIO(pdf), "application/pdf")},
    )
    assert r2.status_code == 201
    assert r2.json()["status"] == "duplicate"


